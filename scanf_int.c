// Practice: Taking integer input from user using scanf
#include<stdio.h>
int main()
{
     // Declaring an integer variable to store age
     int age;
     // Asking the user for input
     printf( "Inter your age =");
     // Taking integer input from user using scanf
     scanf( "%d",&age);
     // Printing the output with a message
     printf( " wow! your age is %d\n",age);
     return 0;

}
