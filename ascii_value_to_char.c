#include<stdio.h>
int main()
{
    // Declaring an integer variable to store ASCII value
     int num;
     // Prompting the user to enter an ASCII value
     printf( "Enter any ASCII value:");
     scanf( "%d",&num);
     // Printing the result as a character using %c
     printf(" The ASCII charcter is :%c",num);
     return 0;
}

